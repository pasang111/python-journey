# Day 1: Learning Variables in Python

# Variables are used to store data in Python.
# We can store text, numbers, decimal values, and True/False values.

name = "Ace"
print(name)

# The variable 'name' stores a string value.

age = 20
print(age)

# The variable 'age' stores an integer value.

height = 5.8
print(height)

# Decimal numbers are stored as float values.

is_student = True
print(is_student)

# Boolean values can only be True or False.


# We can also check the data type of a variable.

print(type(name))
print(type(age))
print(type(height))
print(type(is_student))

# type() tells us what kind of data is stored in a variable.


# Variables can be changed at any time.

score = 75
print(score)

score = 90
print(score)

# The old value is replaced by the new value.


# Variables can be used together.

first_name = "Ace"
last_name = "Snow"

print(first_name, last_name)

# We can combine multiple variables in a single print statement.


# Another example

city = "Kathmandu"
country = "Nepal"

print("City:", city)
print("Country:", country)

# Variables help us store information and reuse it whenever needed.