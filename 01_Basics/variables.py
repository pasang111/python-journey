# Day 1: Learning Variables in Python

# Variables are used to store data in Python.
# We can store text, numbers, decimal values, and True/False values.

name = "Ace"
print(name)

# The variable 'name' stores a string value.

age = 20
print(age)

# The variable 'age' stores an integer value.

height = 5.8
print(height)

# Decimal numbers are stored as float values.

is_student = True
print(is_student)

# Boolean values can only be True or False.


# We can also check the data type of a variable.

print(type(name))
print(type(age))
print(type(height))
print(type(is_student))

# type() tells us what kind of data is stored in a variable.


# Variables can be changed at any time.

score = 75
print(score)

score = 90
print(score)

# The old value is replaced by the new value.


# Variables can be used together.

first_name = "Ace"
last_name = "Snow"

print(first_name, last_name)

# We can combine multiple variables in a single print statement.


# Another example

city = "Kathmandu"
country = "Nepal"

print("City:", city)
print("Country:", country)

# Variables help us store information and reuse it whenever needed.


# Example 1: Storing a favorite game

favorite_game = "Minecraft"
print("Favorite Game:", favorite_game)

# Example 2: Storing marks

math_marks = 95
english_marks = 88

print("Math Marks:", math_marks)
print("English Marks:", english_marks)

# Example 3: Using variables in a sentence

student_name = "Ace"
grade = 12

print(student_name, "is studying in grade", grade)

# Example 4: Adding numbers stored in variables

num1 = 10
num2 = 20

total = num1 + num2
print("Total =", total)

# Example 5: Updating a variable

coins = 50
print("Coins:", coins)

coins = coins + 25
print("Coins after earning:", coins)